    private void btn_themActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_themActionPerformed
        // TODO add your handling code here:
        for (NhaXuatBan first_nxb : ser_nxb.getAllNhaXuatban()) {
            if (first_nxb.getTenNhaXuatBan().equalsIgnoreCase(txt_tenNXB.getText().trim())) {
                JOptionPane.showMessageDialog(this, "Tên nhà xuất bản đã có, vui lòng nhập tên khác");
                return;
            }
        }
        NhaXuatBan second_nxb = checkAddNXB();
        if (second_nxb == null) {
            return;
        }
        ser_nxb.addNhaXuatBan(second_nxb);
        JOptionPane.showMessageDialog(this, "Thêm thành công");

        fillTableNhaXuatBan();
    }//GEN-LAST:event_btn_themActionPerformed

