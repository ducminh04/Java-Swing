    public void fillTableNhaXuatBan() {
        DefaultTableModel dtm = (DefaultTableModel) this.tbl_NXB.getModel();
        dtm.setRowCount(0);
        for (NhaXuatBan nxb : ser_nxb.getAllNhaXuatban()) {
            Object[] rowData = {
                nxb.getId_NXB(),
                nxb.getTenNhaXuatBan(),
                nxb.getNgayTao(),
                nxb.getNgaySua(),
                nxb.isTragThai() == true ? "Hoat dong" : "Khong hoat dong"
            };
            dtm.addRow(rowData);
        }
    }
    
    private NhaXuatBan checkAddNXB() {
        String tenNXB = txt_tenNXB.getText();
        String ngaytao = txt_ngayTao.getText();
        txt_ngayTao.setText(ngaytao.toString());
        if (txt_tenNXB.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập tên nhà xuất bản", "Thông báo", JOptionPane.WARNING_MESSAGE);
            return null;
        }
        NhaXuatBan nxb = new NhaXuatBan();
        nxb.setTenNhaXuatBan(tenNXB);
        nxb.setNgayTao(ngaytao);
        nxb.setTragThai(rdo_hoatDong.isSelected());
        return nxb;
    }

    private NhaXuatBan checkUpdateNXB() {
        int idNxb = Integer.parseInt(txt_idNXB.getText());
        String tenNxb = txt_tenNXB.getText();
        String ngaySua = txt_ngaySua.getText();
        txt_ngaySua.setText(ngaySua.toString());

        if (txt_tenNXB.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập tên nhà xuất bản", "Thông báo", JOptionPane.WARNING_MESSAGE);
            return null;
        }

        NhaXuatBan nxb = new NhaXuatBan();
        nxb.setId_NXB(idNxb);
        nxb.setTenNhaXuatBan(tenNxb);
        nxb.setTragThai(rdo_hoatDong.isSelected());
        nxb.setNgaySua(ngaySua);
        return nxb;
    }

