    public void fillTableNhaCungCap() {
        DefaultTableModel dtm = (DefaultTableModel) this.tbl_nhaCungCap.getModel();
        dtm.setRowCount(0);
        for (NhaCungCap ncc : ser_ncc.getAllNCC()) {
            Object[] rowData = {
                ncc.getId_NCC(),
                ncc.getTenNCC(),
                ncc.getNgayTao(),
                ncc.getNgaySua(),
                ncc.getTrangThai() == true ? "Hop tac" : "Ngung hop tac"
            };
            dtm.addRow(rowData);
        }
    }
    
    private NhaCungCap checkAddNCC() {
        String tenNCC = txt_tenNCC.getText();
        LocalDate ngaytao = LocalDate.now();
        txt_ngayTao.setText(ngaytao.toString());
        if (tenNCC.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập tên nhà cung cấp", "Thông báo", JOptionPane.WARNING_MESSAGE);
            return null;
        }
        
        NhaCungCap ncc = new NhaCungCap();
        ncc.setTenNCC(tenNCC);
        ncc.setNgayTao(ngaytao);
        ncc.setTrangThai(rdo_hopTac.isSelected());
        return ncc;
        
    }
    
    private NhaCungCap checkUpdateNCC() {
        int idNcc = Integer.parseInt(txt_idNCC.getText());
        String tenNCC = txt_tenNCC.getText();
        LocalDate ngaySua = LocalDate.now();
        txt_ngaySua.setText(ngaySua.toString());
        
        if (tenNCC.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập tên nhà cung cấp", "Thông báo", JOptionPane.WARNING_MESSAGE);
            return null;
        }
        
        NhaCungCap ncc = new NhaCungCap();
        ncc.setId_NCC(idNcc);
        ncc.setTenNCC(tenNCC);
        ncc.setTrangThai(rdo_hopTac.isSelected());
        ncc.setNgaySua(ngaySua);
        return ncc;
    }
    
    private void showDetailNCC() {
        
        int index = tbl_nhaCungCap.getSelectedRow();

        // Kiểm tra nếu không có dòng nào được chọn
        if (index == -1) {
            return; // Không làm gì nếu không có dòng nào được chọn
        }

        // Kiểm tra giá trị null trước khi gọi toString()
        Object idNCC = tbl_nhaCungCap.getValueAt(index, 0);
        if (idNCC != null) {
            txt_idNCC.setText(idNCC.toString());
        } else {
            txt_idNCC.setText(""); // Hoặc một giá trị mặc định khác
        }
        
        Object tenNCC = tbl_nhaCungCap.getValueAt(index, 1);
        if (tenNCC != null) {
            txt_tenNCC.setText(tenNCC.toString());
        } else {
            txt_tenNCC.setText("");
        }
        
        Object ngayTao = tbl_nhaCungCap.getValueAt(index, 2);
        if (ngayTao != null) {
            txt_ngayTao.setText(ngayTao.toString());
        } else {
            txt_ngayTao.setText("");
        }
        
        Object ngaySua = tbl_nhaCungCap.getValueAt(index, 3);
        if (ngaySua != null) {
            txt_ngaySua.setText(ngaySua.toString());
        } else {
            txt_ngaySua.setText("");
        }
        
        Object trangThai = tbl_nhaCungCap.getValueAt(index, 4);
        if (trangThai != null && trangThai.equals("Hop tac")) {
            rdo_hopTac.setSelected(true);
        } else {
            rdo_ngungHopTac.setSelected(true);
        }
        //throw new UnsupportedOperationException("Not supported yet."); // Generated from nbfs://nbhost/SystemFileSystem/Templates/Classes/Code/GeneratedMethodBody
    }

