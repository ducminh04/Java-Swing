    private void btn_themActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_themActionPerformed
        // TODO add your handling code here:
        for (NhaCungCap ncc1 : ser_ncc.getAllNCC()) {
            if (ncc1.getTenNCC().equalsIgnoreCase(txt_tenNCC.getText().trim())) {
                JOptionPane.showMessageDialog(this, "Tên nhà cung cấp trùng, vui lòng nhập lại");
                return;
            }
        }
        NhaCungCap ncc = checkAddNCC();
        if (ncc == null) {
            return;
        }
        ser_ncc.addNhaCungCap(ncc);
        JOptionPane.showMessageDialog(this, "Thêm thành công");
        
        fillTableNhaCungCap();

    }//GEN-LAST:event_btn_themActionPerformed

