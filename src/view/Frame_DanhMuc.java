        public void fillTableDanhMuc() {
        DefaultTableModel dtm = (DefaultTableModel) this.tbl_danhMuc.getModel();
        dtm.setRowCount(0);
        for (DanhMuc ncc : ser_dm.getAllDM()) {
            Object[] rowData = {
                ncc.getId_danhMuc(),
                ncc.getTenDanhMuc()
            };
            dtm.addRow(rowData);
        }
    }
         private DanhMuc checkAddDanhMuc() {
        String tenTL = txt_tenDanhMu.getText();
        
        if (txt_tenDanhMu.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập tên the loai", "Thông báo", JOptionPane.WARNING_MESSAGE);
            return null;
        }

        DanhMuc tl = new DanhMuc();
        tl.setTenDanhMuc(tenTL);
       
        return tl;
    }

