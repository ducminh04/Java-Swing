    private void btn_themActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_themActionPerformed
        // TODO add your handling code here:
        for (DanhMuc dm : ser_dm.getAllDM()) {
            if (dm.getTenDanhMuc().equalsIgnoreCase(txt_tenDanhMu.getText().trim())) {
                JOptionPane.showMessageDialog(this, "Tên danh mục đã có, vui lòng nhập tên khác");
                return;
            }
        }
        DanhMuc dm_2 = checkAddDanhMuc();
        if (dm_2 == null) {
            return;
        }
        ser_dm.AddDM(dm_2);
        JOptionPane.showMessageDialog(this, "Thêm thành công");

        fillTableDanhMuc();
    }//GEN-LAST:event_btn_themActionPerformed

