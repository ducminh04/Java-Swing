    public void fillTableTheLoai() {
        DefaultTableModel dtm = (DefaultTableModel) this.tbl_theLoai.getModel();
        dtm.setRowCount(0);
        for (TheLoai tl : ser_theLoai.getAllTheLoai()) {
            Object[] rowData = {
                tl.getId_TheLoai(),
                tl.getTenTheLoai(),
                tl.getNgayTao(),
                tl.getNgaySua()
            };
            dtm.addRow(rowData);
        }
    }

    private TheLoai checkAddTheLoai() {
        String tenTL = txt_tenTheLoai.getText();
        String ngaytao = txt_ngayTao.getText();
        txt_ngayTao.setText(ngaytao.toString());
        if (txt_tenTheLoai.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập tên the loai", "Thông báo", JOptionPane.WARNING_MESSAGE);
            return null;
        }

        TheLoai tl = new TheLoai();
        tl.setTenTheLoai(tenTL);
        tl.setNgayTao(ngaytao);

        return tl;
    }

    private TheLoai checkUpdateTheLoai() {
        int idTL = Integer.parseInt(txt_idTheLoai.getText());
        String tenTL = txt_tenTheLoai.getText();
        String ngaySua = txt_ngaySua.getText();
        txt_ngaySua.setText(ngaySua.toString());

        if (txt_tenTheLoai.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập tên thể loại", "Thông báo", JOptionPane.WARNING_MESSAGE);
            return null;
        }

        TheLoai tl = new TheLoai();
        tl.setId_TheLoai(idTL);
        tl.setTenTheLoai(tenTL);
        tl.setNgaySua(ngaySua);
        return tl;
    }

