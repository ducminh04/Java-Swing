    private void showDetailTheLoai() {
        int index = tbl_theLoai.getSelectedRow();
        // Kiểm tra nếu không có dòng nào được chọn
        if (index == -1) {
            return; // Không làm gì nếu không có dòng nào được chọn
        }
        // Kiểm tra giá trị null trước khi gọi toString()
        Object idTL = tbl_theLoai.getValueAt(index, 0);
        if (idTL != null) {
            txt_idTheLoai.setText(idTL.toString());
        } else {
            txt_idTheLoai.setText(""); // Hoặc một giá trị mặc định khác
        }

        Object tenTL = tbl_theLoai.getValueAt(index, 1);
        if (tenTL != null) {
            txt_tenTheLoai.setText(tenTL.toString());
        } else {
            txt_tenTheLoai.setText("");
        }

        Object ngayTao = tbl_theLoai.getValueAt(index, 2);
        if (ngayTao != null) {
            txt_ngayTao.setText(ngayTao.toString());
        } else {
            txt_ngayTao.setText("");
        }

        Object ngaySua = tbl_theLoai.getValueAt(index, 3);
        if (ngaySua != null) {
            txt_ngaySua.setText(ngaySua.toString());
        } else {
            txt_ngaySua.setText("");
        }

        ///throw new UnsupportedOperationException("Not supported yet."); // Generated from nbfs://nbhost/SystemFileSystem/Templates/Classes/Code/GeneratedMethodBody
    }
