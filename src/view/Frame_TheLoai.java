    private void btn_themActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_themActionPerformed
        // TODO add your handling code here:
        for (TheLoai first_tl : ser_theLoai.getAllTheLoai()) {
            if (first_tl.getTenTheLoai().equalsIgnoreCase(txt_tenTheLoai.getText().trim())) {
                JOptionPane.showMessageDialog(this, "Tên thể loại đã có, vui lòng nhập tên khác");
                return;
            }
        }
        TheLoai second_tl = checkAddTheLoai();
        if (second_tl == null) {
            return;
        }
        ser_theLoai.addTheLoai(second_tl);
        JOptionPane.showMessageDialog(this, "Thêm thành công");

        fillTableTheLoai();
    }//GEN-LAST:event_btn_themActionPerformed

    private void btn_suaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_suaActionPerformed
        // TODO add your handling code here:
        int row = tbl_theLoai.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn dòng cần sửa", "Thông báo", JOptionPane.WARNING_MESSAGE);
            return;
        }
        TheLoai TL = checkUpdateTheLoai();
        if (TL == null) {
            return;
        }
        ser_theLoai.updateTheLoai(TL);
        JOptionPane.showMessageDialog(this, "Sửa thành công");
        fillTableTheLoai();
    }//GEN-LAST:event_btn_suaActionPerformed

